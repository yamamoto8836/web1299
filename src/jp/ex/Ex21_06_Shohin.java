package jp.ex;

public class Ex21_06_Shohin {
	private int hinban, tanka;
	private String hinmai;
	
	public Ex21_06_Shohin(int hinban, int tanka, String hinmai) {
		this.hinban = hinban;
		this.tanka = tanka;
		this.hinmai = hinmai;
	}

	public int getHinban() {
		return hinban;
	}

	public int getTanka() {
		return tanka;
	}

	public String getHinmai() {
		return hinmai;
	}

}
